export const getTranslation = (lang: 'en' | 'hi', key: keyof typeof translations.en, ...args: string[]) => {
  let text = translations[lang][key] || translations.en[key];
  args.forEach((arg, i) => {
    text = text.replace(`{${i}}`, arg);
  });
  return text;
};
